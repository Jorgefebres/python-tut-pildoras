# PARA CREAR UN EJECUTABLE SE DEBE
# instalar pyinstaller -> pip install pyinstaller
# luego ir a la carpeta del archivo que se quiere convertir en un ejecutable
# ejecutar cmd y -> pyinstaller archivo.py
# para que nos de un ejecutable sin el fondo de la console -> ejecutar cmd y -> pyinstaller --windowed archivo.py
# para que nos de un solo archivo ejecutable sin el fondo de la console -> ejecutar cmd y -> pyinstaller --windowed --onefile archivo.py
# para agregar un icono al ejecutable -> agregar el archivo ico a la carpeta y ejecutar cmd y -> pyinstaller --windowed --onefile --icon=./logo.ico archivo.py
