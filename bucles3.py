# tipo RANGE
for i in range(5):
    print(f"Valor de la variable: {i}")
print("------------------------------")
# tipo RANGE del 5 al 15
for i in range(5, 15):
    print(f"Valor de la variable: {i}")
print("------------------------------")
# tipo RANGE del 5 al 15 de 2 en 2
for i in range(5, 15, 2):
    print(f"Valor de la variable: {i}")
